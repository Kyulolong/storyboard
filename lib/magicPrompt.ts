// 사용자가 자기 ChatGPT/Claude에 그대로 붙여넣는 "마법 프롬프트".
// 우리 앱이 파싱할 수 있는 정확한 JSON을 뽑아내도록 스키마와 릴스 연출 규칙을 심어둔다.
// => LLM 통합/키/비용 없이 유저의 무료 LLM을 빌려쓰는 핸드오프 방식.
export const MAGIC_PROMPT = `너는 숏폼(릴스/쇼츠) 영상 콘티 전문가야.
아래 [내 아이디어]를 세로(9:16) 릴스 콘티로 만들어줘.

# 규칙
- 컷은 6개 내외로 나눠줘. 릴스는 템포가 빠르니 컷당 1~4초 정도로.
- 1번 컷은 반드시 "훅(hook)"으로. 첫 1~2초에 스크롤을 멈추게 하는 강한 장면/문구.
- 등장인물은 대부분 촬영자 본인이라고 가정해(삼각대/셀카). 필요하면 인서트/B롤 컷을 섞어.
- 각 컷에 화면 자막(caption)을 넣어줘. 릴스는 자막이 연출의 일부야.
- 혼자 찍는 사람을 위한 촬영 팁(shooting_tip)을 한 줄로.

# 출력 형식 (아주 중요)
- 설명/인사말 없이 아래 JSON "만" 출력해.
- 반드시 이 스키마를 지켜. 값은 한국어로.

\`\`\`json
{
  "cuts": [
    {
      "no": 1,
      "duration_sec": 2,
      "is_hook": true,
      "shot": "클로즈업",
      "description": "카메라를 향해 놀란 표정. 손으로 입을 가림",
      "dialogue": "이거 진짜 아무도 안 알려줘요",
      "caption": "99%가 모르는 사실",
      "shooting_tip": "삼각대, 눈높이, 얼굴 클로즈업"
    }
  ]
}
\`\`\`

# 필드 설명
- no: 컷 번호(1부터)
- duration_sec: 컷 길이(초)
- is_hook: 훅 컷이면 true (보통 1번 컷)
- shot: 샷 사이즈 (익스트림롱샷/롱샷/미디엄샷/클로즈업/익스트림클로즈업 등)
- description: 화면에 보이는 그림 묘사. 스케치로 그릴 소스라 "무엇이 어떻게 보이는지" 구체적으로.
- dialogue: 대사 또는 나레이션 (없으면 "")
- caption: 화면 자막 (없으면 "")
- shooting_tip: 촬영 팁 한 줄

# 내 아이디어
<여기에 아이디어를 적으세요. 예: "다이어트 중에도 먹을 수 있는 야식 3가지를 소개하는 릴스">`;

// 유저가 입력한 아이디어를 마법 프롬프트의 마지막 자리표시자에 끼워 넣는다.
// (자동 생성이 rate limit에 걸렸을 때, 아이디어가 채워진 프롬프트를 바로 복사해주기 위함)
export function magicPromptWithIdea(idea: string): string {
  const trimmed = (idea || "").trim();
  if (!trimmed) return MAGIC_PROMPT;
  return MAGIC_PROMPT.replace(/<여기에 아이디어를 적으세요[^>]*>/, trimmed);
}
